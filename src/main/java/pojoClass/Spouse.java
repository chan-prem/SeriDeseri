package pojoClass;

public class Spouse {

	
		int sphNo;
		String sname;
		public Spouse(int sphNo, String sname) {
			this.sphNo = sphNo;
			this.sname = sname;
		}
		public int getSphNo() {
			return sphNo;
		}
		public void setSphNo(int sphNo) {
			this.sphNo = sphNo;
		}
		public String getSname() {
			return sname;
		}
		public void setSname(String sname) {
			this.sname = sname;
		}
		
		
}
