package pojoClass;

public class Battery2Earphone {
	
	String type;
	int no;
	public Battery2Earphone(String type, int no) {
		this.type = type;
		this.no = no;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	
	

}
